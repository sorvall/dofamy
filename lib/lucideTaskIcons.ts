function toPascalCase(raw: string): string {
  const t = raw.trim();
  if (!t) return "";
  if (/^[A-Z][a-zA-Z0-9]*$/.test(t)) return t;
  if (/^[a-z][a-zA-Z0-9]*$/.test(t)) {
    const first = t[0];
    return first ? first.toUpperCase() + t.slice(1) : t;
  }
  return t;
}

/** Иконка по имени из DeepSeek или fallback Sparkles. */
export function resolveLucideTaskIcon(name: string): TaskIcon {
  const key = toPascalCase(name);
  return LUCIDE_ICON_MAP[key] ?? Sparkles;
}

export function isAsciiIconName(s: string): boolean {
  return /^[A-Za-z][a-zA-Z0-9]*$/.test(s.trim());
}
